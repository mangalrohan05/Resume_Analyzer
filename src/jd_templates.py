def build_eval_pairs(df, jd_templates=JD_TEMPLATES, n_negative=2):
    """
    Build evaluation pairs from the resume dataframe and JD templates.

    Args:
        df: Resume dataframe with 'Resume_str' and 'Category' columns.
        jd_templates: dict of {category: [jd1, jd2, jd3]}
        n_negative: number of negative JDs to pair with each resume sample

    Returns:
        List of (resume_text, jd_text, is_match: bool)
    """
    import random
    random.seed(42)

    eval_pairs = []
    categories = list(jd_templates.keys())

    for category, jds in jd_templates.items():
        # Sample up to 3 resumes per category
        cat_resumes = df[df['Category'] == category]['Resume_str'].tolist()
        if not cat_resumes:
            continue
        sample_resumes = random.sample(cat_resumes, min(3, len(cat_resumes)))

        for resume in sample_resumes:
            # Positive pairs — all 3 JD variants for the matching category
            for jd in jds:
                eval_pairs.append((resume, jd, True))

            # Negative pairs — JDs from different categories
            negative_cats = [c for c in categories if c != category]
            neg_sample = random.sample(negative_cats, min(n_negative, len(negative_cats)))
            for neg_cat in neg_sample:
                neg_jd = random.choice(jd_templates[neg_cat])
                eval_pairs.append((resume, neg_jd, False))

    random.shuffle(eval_pairs)
    return eval_pairs